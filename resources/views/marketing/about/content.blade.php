            <span class="text-indigo-600">Codehub Mentoring Scheme</span>
        </h1>

        <p class="text-gray-500 sm:text-base md:text-lg md:max-w-3xl mt-6">
            Our mentoring programme has been running since 2018. The idea is simple: a more experienced mentor
            agrees to mentor a more junior engineer via a regular cadence of meetings; the outcome is that the
            mentor helps the mentee to grow, typically more quickly and in a more focussed way than would
            otherwise be the case.
        </p>
        <p class="text-gray-500 sm:text-base md:text-lg md:max-w-3xl mt-6">
            The feedback has been overwhelmingly positive: not only does the mentee learn and develop but it is
            also an opportunity for the mentor to develop their communication and leadership skills and gain
            perspective which can help them with situations they face in their own careers.
        </p>

        <h2 class="text-3xl mt-6">
            Characteristics of a CodeHub mentoring relationship
        </h2>
        <ul class="list-disc space-y-2 text-gray-500">
            <li>One-on-one support with a personalised development plan, driven by the mentee</li>
            <li>Continues for 3 - 4 months</li>
            <li>Regular meetings - typically, 1 - 2 hours every two weeks</li>
        </ul>

        <h2 class="text-3xl mt-6">
            Process
        </h2>
        <ol class="list-decimal space-y-2 text-gray-500">
            <li>Signup: mentors and mentees register their interest, experience and needs</li>
            <li>Kickoff meeting: Introductory meeting - opportunity for explanations, questions and initial ‘get
                to know
                you’.</li>
            <li>Matching: Mentors and mentees are paired up, based on stated interests and any other relevant
                information.</li>
            <li>Mentoring period: The critical time with regular meetings, commitments from both parties,
                open/honest
                communication.</li>
            <li>Feedback: A review is carried out at the end of the programme.</li>
        </ol>

        <h2 class="text-3xl mt-6">
            Outcomes and benefits
        </h2>
        <p class="text-gray-500 sm:text-base md:text-lg md:max-w-3xl">
            Mentorships can have profound impacts. For example, some past mentees have found jobs through their
            mentors, or gained the necessary practice and confidence for interviews that then led to a job.
            Mentees often gain confidence as well. Some other benefits were: Being pointed in the right
            direction, help with portfolio sites, gaining skills by being set small challenges, making
            significant progress in a programming language and getting more enjoyment out of programming. The
            mentors also usually gain from the process. They can themselves learn from the questions the mentees
            ask, learn to break topics into smaller chunks, and often simply enjoy contributing to somebody
            else’s progress.
        </p>
        <div class="grid md:grid-cols-2 gap-16">
            <div>
                <h2 class="text-3xl mt-6">
                    Guidelines and Tips for Mentors
                </h2>
                <ul class="list-disc space-y-2 text-gray-500">
                    <li>Let the mentee guide the process</li>
                    <li>Help them to set realistic goals</li>
                    <li>Treat your mentee as an equal: ask for their opinion</li>
                    <li>Never type for them, no matter how slow this might make the process</li>
                    <li>Encourage them to look up things online if you know they’ll find the answer easily - you
                        don’t
                        want them
                        to become</li>
                    <li>reliant on you</li>
                    <li>Encourage planning before jumping into coding: think about the code, drawing
                        diagrams/flow
                        charts</li>
                    <li>Try and keep your own personal preferences out of discussions of how to improve their
                        code.
                        Always
                        justify comments with</li>
                    <li>objective measures. For example, readability, changeability, testability, etc.</li>
                    <li>Try and always say at least one positive thing when giving criticism</li>
                    <li>Say as much as they need to know to solve the problem, but nothing more</li>
                    <li>Treat this as a learning experience for yourself</li>
                    <li>Try and frame solutions as questions - e.g. rather than “You need to use array.length”
                        you
                        could
                        say
                        “How can you find</li>
                    <li>out how many items there are in the array?”</li>
                    <li>Always try and remember what it was like when you were first learning whatever you are
                        going
                        through
                    </li>
                    <li>It can be reassuring to tell the mentee about times that you found what they’re trying
                        to do
                        difficult
                    </li>
                    <li>Avoid sarcasm</li>
                </ul>
            </div>
            <div>
                <h2 class="text-3xl mt-6">
                    Guidelines and Tips for Mentees
                </h2>

                <ul class="list-disc space-y-2 text-gray-500">
                    <li>You should be in control of the agenda, take responsibility for your own development,
                        rather
                        than
                        expecting ‘quick fixes’ from a mentor</li>
                    <li>Make sure that your goals are realistic: you should talk to your mentor about this
                        before
                        your
                        first
                        meeting</li>
                    <li>Be committed, e.g. attending planned sessions, taking the actions planned with the
                        mentor
                    </li>
                    <li>Be professional in the relationship with the mentor, e.g. being punctual, respecting
                        agreed
                        ground
                        rules, talking openly and honestly with the mentor</li>
                    <li>Make sure you use some of your own time to improve between seeing your mentor</li>
                    <li>If you lose interest, let your mentor know as soon as possible so that they can mentor
                        someone
                        else</li>
                    <li>Remember, learning from your mistakes is an important part of the process, so never be
                        worried
                        about
                        showing code that doesn’t work or that you are unsure of
                    <li>The best way to get good at programming is to try and solve real problems: many online
                        courses
                        tell you
                        what you need to
                        use in order to solve a problem (e.g. if you’ve just been learning about arrays, it’s
                        probably
                        an array
                        you’ll need) -
                        the real skill in programming is knowing what to use without being told</li>
                    <li>Practice, practice, practice</li>
                </ul>
            </div>
